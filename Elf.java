public class Elf {


    private int id;
    private int power;
    private int magic;
    private int charisma;
    private int age;
    private int name;


    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public int getPower() {
        return power;
    }
    public void setPower() {
        this.power = power;
    }

    public int getMagic() {
        return magic ;
    }
    public void setMagic(){
        this.magic = magic;
    }

    public int getCharisma() {
        return charisma;
    }
    public void setCharisma() {
        this.charisma = charisma;
    }

    public int getAge() {
        return age;
    }
    public void setAge() {
        this.age = age;
    }

    public int getName() {
        return name;
    }
    public void setName() {
        this.name = name;
    }

}


